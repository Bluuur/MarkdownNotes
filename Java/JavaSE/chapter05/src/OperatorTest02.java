public class OperatorTest02{
	public static void main(String[] args){
		int a = 10;
		int b = 10;
		System.out.println(a > b);	//false
		System.out.println(a >= b);	// ture
		System.out.println(a < b);	//false
		System.out.println(a <= b);	//ture
		System.out.println(a == b);	//ture
		System.out.println(a != b);	//false
	}
}