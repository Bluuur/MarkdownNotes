public class HomeWork03{
	public static void main(String[] args){
		int i = 5;
		int j = 5;
		int m = 5;
		int n = 5;
		i++;//i=6
		j = j + 1;//j=6
		m--;//m=4
		n = n - 1;//n=4
		System.out.println(i);//6
		System.out.println(i++);//输出6，i=7
		System.out.println(++i);//输出8，i=8
		System.out.println(i--);//输出8，i=7
		System.out.println(--i);//输出6，i=6
		System.out.println();
		System.out.println(j);//输出6
		System.out.println(j++);//输出6，j=7
		System.out.println(j--);//输出7，j=6
		System.out.println(--j);//输出5，j=5
		System.out.println(m);//4
		System.out.println(n);//4

	}
}